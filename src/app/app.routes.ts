import { Routes } from '@angular/router';

export const routes: Routes = [
  {
    path: 'home',
    loadComponent: () =>
      import('./pages/home/home.page').then((m) => m.HomePage),
  },
  {
    path: '',
    redirectTo: 'home',
    pathMatch: 'full',
  },
  {
    path: 'add-plant',
    loadComponent: () =>
      import('./pages/add-plant/add-plant.page').then((m) => m.AddPlantPage),
  },
];
